public class Register {
    private String username;
    private String password;
    private String name;
    private int age;
    private int ID;

    public Register(String username, String password, String name, int age, int ID){
        this.username = username;
        this.password = password;
        this.name = name;
        this.age = age;
        this.ID = ID;
    }

}
